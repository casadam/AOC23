int GETDIGIT(char line[]) {
    char *iter = line;
    int calibration = 0;
    int tempDigit = -1;
    bool firstGet = false;
    while (*iter != '\0') {
        if (*iter >= 48 && *iter <= 57) {
            tempDigit = *iter - 48;
        }
        if (tempDigit != -1 && !firstGet) {
            calibration = tempDigit * 10;
            firstGet = true;
        }
        iter++;
    }
    calibration += tempDigit;
    return calibration;
}

int GETREALDIGITS(char line[]) {
    int calibration = 0;
    bool firstGet = false;
    int currNum = -1;
    int size = strlen(line);
    for (int i = 0; i < size; ++i) {
        if (line[i] >= 48 && line[i] <= 57) {
            currNum = line[i] - 48;
        }
        else {
            if (i + 2 < size) {
                char temp[4];
                strncpy(temp, line + i, 3);
                if (strcmp(temp, "one") == 0) currNum = 1;
                else if (strcmp(temp, "two") == 0) currNum = 2;
                else if (strcmp(temp, "six") == 0) currNum = 6;
            }
            if (i + 3 < size) {
                char temp[5];
                strncpy(temp, line + i, 4);
                if (strcmp(temp, "four") == 0) currNum = 4;
                else if (strcmp(temp, "five") == 0) currNum = 5;
                else if (strcmp(temp, "nine") == 0) currNum = 9;
            }
            if (i + 4 < size) {
                char temp[6];
                strncpy(temp, line + i, 5);
                if (strcmp(temp, "three") == 0) currNum = 3;
                else if (strcmp(temp, "seven") == 0) currNum = 7;
                else if (strcmp(temp, "eight") == 0) currNum = 8;
            }
        }
        if (!firstGet && currNum != -1) {
            calibration = currNum * 10;
            firstGet = true;
        }
    }
    calibration += currNum;
    return calibration;
}

